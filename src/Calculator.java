
public class Calculator {
	int num1;
	int num2;
	int result;

	public int add(int num1, int num2) {
		result=num1+num2;
		return result;
	}

	public int minus(int num1, int num2) {
		result=num1-num2;
		return result;
	}

	public int multiply(int num1, int num2) {
		result=num1*num2;
		return result;
	}

}
